-- Create ai_agent_runs (Log)
CREATE TABLE IF NOT EXISTS public.ai_agent_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id UUID,
    lead_id BIGINT REFERENCES public.leads(id) ON DELETE SET NULL,
    stage_id BIGINT REFERENCES public.ai_stage_config(id) ON DELETE SET NULL, -- optional
    trigger_type TEXT NOT NULL, -- stage_entered | inbound_message | scheduled_job
    input_snapshot JSONB, -- Context used
    llm_output JSONB, -- The raw JSON from AI
    actions_executed JSONB, -- What backend actually did
    status TEXT NOT NULL, -- success | failed
    error_message TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Create ai_summaries (Reports)
CREATE TABLE IF NOT EXISTS public.ai_summaries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id UUID,
    period_type TEXT NOT NULL, -- daily | weekly | monthly
    period_start TIMESTAMP WITH TIME ZONE,
    period_end TIMESTAMP WITH TIME ZONE,
    summary_text TEXT,
    insights_json JSONB,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- RLS for new tables
ALTER TABLE public.ai_agent_runs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.ai_summaries ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow full access to ai_agent_runs" ON public.ai_agent_runs FOR ALL USING (true);
CREATE POLICY "Allow full access to ai_summaries" ON public.ai_summaries FOR ALL USING (true);

